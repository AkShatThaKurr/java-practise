//  Program to demonstrate charAt() method in String
//  charAt(index) returns the character at the given index
package strings.methodsinstring;

public class method3_1charAt {
    public static void main(String[] args) {
        String name = "akshat";
        System.out.println("Character at index 0: " + name.charAt(0));
        System.out.println("Character at index 2: " + name.charAt(2));
    }
}
