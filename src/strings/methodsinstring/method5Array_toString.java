// Program to demonstrate Arrays.toString() method
// We need to import java.util.Arrays to use Arrays.toString() method
package strings.methodsinstring;

import java.util.Arrays;

public class method5Array_toString {
    public static void main(String[] args) {
        char[] arr = {'a', 'k', 's', 'h', 'a', 't'};
        System.out.println(Arrays.toString(arr));
    }
}
