// Check whether number is positive or negative.

public class IfElse03 {
    public static void main(String[] args) {
        int a = -10;
        if(a>0){
            System.out.println("Positive");
        }
        else if(a==0){
            System.out.println("Zero");
        }
        else{
            System.out.println("Negative");
        }
    }
}
