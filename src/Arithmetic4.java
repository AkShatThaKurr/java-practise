// Program to calculate simple interest (si = (p*r*t)/100 )

public class Arithmetic4 {
    public static void main(String[] args) {
        int p = 10000;
        double r = 2.5;
        double t = 2;
        double si = (p*r*t)/100;
        System.out.println("The Simple Interest is: " + si);
    }
}
