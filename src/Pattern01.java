// Write a program to print a straight horizontal line of stars. (* * * * *)

public class Pattern01 {
    public static void main(String[] args) {
        int n=1;
        for(int i=1; i<=n; i++){
            for(int j=1; j<=5; j++){
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
